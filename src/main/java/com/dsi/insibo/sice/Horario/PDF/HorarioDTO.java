package com.dsi.insibo.sice.Horario.PDF;

public class HorarioDTO {
    private String duiDocente, idHorarioBase, codMateria, nomMateria, nombreDocente, apellidoDocente, grado, seccion, nombreCarrera, codigo;

    public String getDuiDocente() {
        return duiDocente;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public void setDuiDocente(String duiDocente) {
        this.duiDocente = duiDocente;
    }

    public String getIdHorarioBase() {
        return idHorarioBase;
    }

    public void setIdHorarioBase(String idHorarioBase) {
        this.idHorarioBase = idHorarioBase;
    }

    public String getCodMateria() {
        return codMateria;
    }

    public void setCodMateria(String codMateria) {
        this.codMateria = codMateria;
    }

    public String getNomMateria() {
        return nomMateria;
    }

    public void setNomMateria(String nomMateria) {
        this.nomMateria = nomMateria;
    }

    public String getNombreDocente() {
        return nombreDocente;
    }

    public void setNombreDocente(String nombreDocente) {
        this.nombreDocente = nombreDocente;
    }

    public String getApellidoDocente() {
        return apellidoDocente;
    }

    public void setApellidoDocente(String apellidoDocente) {
        this.apellidoDocente = apellidoDocente;
    }

    public String getGrado() {
        return grado;
    }

    public void setGrado(String grado) {
        this.grado = grado;
    }

    public String getSeccion() {
        return seccion;
    }

    public void setSeccion(String seccion) {
        this.seccion = seccion;
    }

    public String getNombreCarrera() {
        return nombreCarrera;
    }

    public void setNombreCarrera(String nombreCarrera) {
        this.nombreCarrera = nombreCarrera;
    }


}
